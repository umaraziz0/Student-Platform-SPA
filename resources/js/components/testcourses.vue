<template>
    <div class="courses-style">
        <div class="table-style">
            <input
                class="input"
                type="text"
                v-model="search"
                placeholder="Search..."
                @input="resetPagination()"
                style="width: 250px;"
            />
            <div class="control">
                <div class="select">
                    <select v-model="length" @change="resetPagination()">
                        <option value="10">10</option>
                        <option value="20">20</option>
                        <option value="30">30</option>
                    </select>
                </div>
            </div>
        </div>
        <table class="table table-bordered table-responsive">
            <thead>
                <tr>
                    <th
                        v-for="column in columns"
                        :key="column.name"
                        @click="sortBy(column.name)"
                        :class="
                            sortKey === column.name
                                ? sortOrders[column.name] > 0
                                    ? 'sorting_asc'
                                    : 'sorting_desc'
                                : 'sorting'
                        "
                        style="width: 20%; cursor:pointer;"
                    >
                        {{ column.label }}
                    </th>
                    <th>Actions</th>
                </tr>
            </thead>
            <tbody>
                <tr v-for="course in paginatedCourses" :key="course.course_id">
                    <td>{{ course.course_id }}</td>
                    <td>{{ course.course_name }}</td>
                    <td>{{ course.credits }}</td>
                    <td>{{ course.teacher }}</td>
                    <td>
                        <div class="btn-group">
                            <button
                                type="button"
                                class="btn btn-info"
                                v-on:click="editModal(user)"
                            >
                                <i class="fas fa-edit text-white"></i>
                            </button>
                            <button
                                type="button"
                                class="btn btn-danger"
                                v-on:click="deleteUser(user.id)"
                            >
                                <i class="fas fa-trash"></i>
                            </button>
                        </div>
                    </td>
                </tr>
            </tbody>
        </table>
        <div>
            <nav class="pagination" v-if="!tableShow.showdata">
                <span class="page-stats">
                    {{ pagination.from }} - {{ pagination.to }} of
                    {{ pagination.total }}
                </span>
                <a
                    v-if="pagination.prevPageUrl"
                    class="btn btn-sm btn-primary pagination-previous"
                    @click="--pagination.currentPage"
                    >Prev</a
                >
                <a
                    class="btn btn-sm btn-primary pagination-previous"
                    v-else
                    disabled
                    >Prev</a
                >
                <a
                    v-if="pagination.nextPageUrl"
                    class="btn btn-sm pagination-next"
                    @click="++pagination.currentPage"
                    >Next</a
                >
                <a
                    class="btn btn-sm btn-primary pagination-next"
                    v-else
                    disabled
                    >Next</a
                >
            </nav>
            <nav class="pagination" v-else>
                <span class="page-stats">
                    {{ pagination.from }} - {{ pagination.to }} of
                    {{ filteredCourses.length }}
                    <span
                        v-if="`filteredCourses.length < pagination.total`"
                    ></span>
                </span>
                <a
                    v-if="pagination.prevPage"
                    class="btn btn-sm btn-primary pagination-previous"
                    @click="--pagination.currentPage"
                    >Prev</a
                >
                <a
                    class="btn btn-sm pagination-previous btn-primary"
                    v-else
                    disabled
                    >Prev</a
                >
                <a
                    v-if="pagination.nextPage"
                    class="btn btn-sm btn-primary pagination-next"
                    @click="++pagination.currentPage"
                    >Next</a
                >
                <a
                    class="btn btn-sm pagination-next btn-primary"
                    v-else
                    disabled
                    >Next</a
                >
            </nav>
        </div>
    </div>
</template>
<script>
export default {
    created() {
        this.getCourses();
        Fire.$on("reloadCourses", () => {
            this.getCourses();
        });
    },

    data() {
        let sortOrders = {};
        let columns = [
            { label: "ID", name: "course_id" },
            { label: "Course Name", name: "course_name" },
            { label: "Credits", name: "credits" },
            { label: "Teacher", name: "teacher" }
        ];
        columns.forEach(column => {
            sortOrders[column.name] = -1;
        });

        return {
            courses: [],
            columns: columns,
            sortKey: "course_id",
            sortOrders: sortOrders,
            length: 10,
            search: "",
            tableShow: { showdata: true },
            pagination: {
                currentPage: 1,
                total: "",
                nextPage: "",
                prevPage: "",
                from: "",
                to: ""
            }
        };
    },

    methods: {
        getCourses() {
            axios
                .get("api/testcourses", { params: this.tableShow })
                .then(response => {
                    console.log("The data: ", response.data);
                    this.courses = response.data;
                    this.pagination.total = this.courses.length;
                })
                .catch(errors => {
                    console.log(errors);
                });
        },

        paginate(array, length, pageNumber) {
            this.pagination.from = array.length
                ? (pageNumber - 1) * length + 1
                : " ";
            this.pagination.to =
                pageNumber * length > array.length
                    ? array.length
                    : pageNumber * length;
            this.pagination.prevPage = pageNumber > 1 ? pageNumber : "";
            this.pagination.nextPage =
                array.length > this.pagination.to ? pageNumber + 1 : "";
            return array.slice((pageNumber - 1) * length, pageNumber * length);
        },

        resetPagination() {
            this.pagination.currentPage = 1;
            this.pagination.prevPage = "";
            this.pagination.nextPage = "";
        },

        sortBy(key) {
            this.resetPagination();
            this.sortKey = key;
            this.sortOrders[key] = this.sortOrders[key] * -1;
        },

        getIndex(array, key, value) {
            return array.findIndex(i => i[key] == value);
        }
    },

    computed: {
        filteredCourses() {
            let courses = this.courses;
            if (this.search) {
                courses = courses.filter(row => {
                    return Object.keys(row).some(key => {
                        return (
                            String(row[key])
                                .toLowerCase()
                                .indexOf(this.search.toLowerCase()) > -1
                        );
                    });
                });
            }

            let sortKey = this.sortKey;
            let order = this.sortOrders[sortKey] || 1;
            if (sortKey) {
                courses = courses.slice().sort((a, b) => {
                    let index = this.getIndex(this.columns, "name", sortKey);
                    a = String(a[sortKey]).toLowerCase();
                    b = String(b[sortKey]).toLowerCase();
                    if (
                        this.columns[index].type &&
                        this.columns[index].type === "date"
                    ) {
                        return (
                            (a === b
                                ? 0
                                : new Date(a).getTime() > new Date(b).getTime()
                                ? 1
                                : -1) * order
                        );
                    } else if (
                        this.columns[index].type &&
                        this.columns[index].type === "number"
                    ) {
                        return (+a === +b ? 0 : +a > +b ? 1 : -1) * order;
                    } else {
                        return (a === b ? 0 : a > b ? 1 : -1) * order;
                    }
                });
            }

            return courses;
        },

        paginatedCourses() {
            return this.paginate(
                this.filteredCourses,
                this.length,
                this.pagination.currentPage
            );
        }
    }
};
</script>
